package com.example.dataobat;

public class ModelObat {
    private String fieldKode = "kode";
    private String fieldNama = "nama";

    private String fieldJumlah = "jumlah";
    private String fieldSatuan = "satuan";
    private String fieldHarga = "harga";

    public String getFieldKode() {
        return fieldKode;
    }

    public void setFieldKode(String fieldKode) {
        this.fieldKode = fieldKode;
    }

    public String getFieldNama() {
        return fieldNama;
    }

    public void setFieldNama(String fieldNama) {
        this.fieldNama = fieldNama;
    }

    public String getFieldJumlah() {
        return fieldJumlah;
    }

    public void setFieldJumlah(String fieldJumlah) {
        this.fieldJumlah = fieldJumlah;
    }

    public String getFieldSatuan() {
        return fieldSatuan;
    }

    public void setFieldSatuan(String fieldSatuan) {
        this.fieldSatuan = fieldSatuan;
    }

    public String getFieldHarga() {
        return fieldHarga;
    }

    public void setFieldHarga(String fieldHarga) {
        this.fieldHarga = fieldHarga;
    }
}
